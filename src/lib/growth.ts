export function patientGrowthSex(patient: Patient): GrowthSex | null {
  if (patient.gender === "male") return "male";
  if (patient.gender === "female") return "female";
  return null;
}

export function ageYearsAt(birthDate: string | undefined, date: string | undefined): number | null {
  if (!birthDate || !date) return null;
  const born = new Date(`${birthDate}T00:00:00Z`);
  const measured = new Date(date);
  if (Number.isNaN(born.getTime()) || Number.isNaN(measured.getTime())) return null;
  const age = (measured.getTime() - born.getTime()) / 31557600000;
  return age >= 0 ? age : null;
}

export function observationLoinc(obs: Observation): string | undefined {
  return obs.code?.coding?.find((c) => c.system === "http://loinc.org")?.code ?? obs.code?.coding?.[0]?.code;
}

function normalizedValue(obs: Observation, metric: GrowthMetric): number | null {
  const value = obs.valueQuantity?.value;
  if (value === undefined || Number.isNaN(value)) return null;
  const unit = (obs.valueQuantity?.code ?? obs.valueQuantity?.unit ?? "").toLowerCase();
  if (metric === "weight") {
    if (unit === "g") return value / 1000;
    if (unit === "lb" || unit === "[lb_av]") return value * 0.45359237;
    return value;
  }
  if (metric === "height") {
    if (unit === "m") return value * 100;
    if (unit === "in" || unit === "[in_i]") return value * 2.54;
    return value;
  }
  return value;
}

function readingFromObservation(patient: Patient, obs: Observation, metric: GrowthMetric): GrowthReading | null {
  const date = obs.effectiveDateTime ?? obs.issued;
  const ageYears = ageYearsAt(patient.birthDate, date);
  const value = normalizedValue(obs, metric);
  if (ageYears === null || value === null) return null;
  return { ageYears, value, date: date ?? "" };
}

export function extractGrowthSeries(patient: Patient, observations: Observation[]): GrowthSeries {
  const weight = observations
    .filter((obs) => observationLoinc(obs) === WEIGHT_CODE)
    .map((obs) => readingFromObservation(patient, obs, "weight"))
    .filter((r): r is GrowthReading => r !== null);

  const height = observations
    .filter((obs) => observationLoinc(obs) === HEIGHT_CODE)
    .map((obs) => readingFromObservation(patient, obs, "height"))
    .filter((r): r is GrowthReading => r !== null);

  const explicitBmi = observations
    .filter((obs) => observationLoinc(obs) === BMI_CODE)
    .map((obs) => readingFromObservation(patient, obs, "bmi"))
    .filter((r): r is GrowthReading => r !== null);

  const calculatedBmi = calculateBmiReadings(weight, height);
  const bmi = explicitBmi.length > 0 ? explicitBmi : calculatedBmi;

  return {
    sex: patientGrowthSex(patient),
    weight: sortReadings(weight),
    height: sortReadings(height),
    bmi: sortReadings(bmi),
  };
}

function sortReadings(readings: GrowthReading[]): GrowthReading[] {
  return [...readings].sort((a, b) => a.ageYears - b.ageYears);
}

function calculateBmiReadings(weight: GrowthReading[], height: GrowthReading[]): GrowthReading[] {
  const heightByDate = new Map(height.map((h) => [h.date.slice(0, 10), h]));
  return weight
    .map((w) => {
      const h = heightByDate.get(w.date.slice(0, 10));
      if (!h || h.value <= 0) return null;
      const meters = h.value / 100;
      return {
        ageYears: w.ageYears,
        date: w.date,
        value: Number((w.value / (meters * meters)).toFixed(1)),
      };
    })
    .filter((r): r is GrowthReading => r !== null);
}

export function interpolateReference(
  rows: GrowthReferenceRow[],
  ageYears: number
): GrowthReferenceRow | null {
  if (rows.length === 0 || ageYears < rows[0].ageYears || ageYears > rows[rows.length - 1].ageYears) {
    return null;
  }
  const upper = rows.find((row) => row.ageYears >= ageYears);
  if (!upper) return null;
  const upperIndex = rows.indexOf(upper);
  const lower = rows[Math.max(0, upperIndex - 1)];
  if (upper.ageYears === lower.ageYears) return upper;
  const t = (ageYears - lower.ageYears) / (upper.ageYears - lower.ageYears);
  return {
    ageYears,
    p3: lerp(lower.p3, upper.p3, t),
    p10: lerp(lower.p10, upper.p10, t),
    p50: lerp(lower.p50, upper.p50, t),
    p90: lerp(lower.p90, upper.p90, t),
    p97: lerp(lower.p97, upper.p97, t),
  };
}

export function estimateCentile(
  metric: GrowthMetric,
  sex: GrowthSex,
  reading: GrowthReading
): string {
  const ref = interpolateReference(getGrowthReference(metric, sex), reading.ageYears);
  if (!ref) return "outside CDC range";
  const bands = [
    [3, ref.p3],
    [10, ref.p10],
    [50, ref.p50],
    [90, ref.p90],
    [97, ref.p97],
  ] as const;
  if (reading.value < ref.p3) return "<3rd";
  if (reading.value > ref.p97) return ">97th";
  for (let i = 1; i < bands.length; i++) {
    const [p0, v0] = bands[i - 1];
    const [p1, v1] = bands[i];
    if (reading.value <= v1) {
      const centile = p0 + ((reading.value - v0) / (v1 - v0)) * (p1 - p0);
      return `${Math.round(centile)}th`;
    }
  }
  return "97th";
}

function lerp(a: number, b: number, t: number): number {
  return a + (b - a) * t;
}
